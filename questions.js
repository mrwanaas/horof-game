class QuestionManager {
  constructor() {
    this.used = new Set();
    this.recentLetters = [];
  }

  getCategories() {
    return [...new Set(QUESTION_BANK.map(q => q.cat))];
  }

  getRandom(wonLetters, category) {
    wonLetters = wonLetters || [];
    let pool = QUESTION_BANK.filter(q => {
      if (this.used.has(q.q)) return false;
      if (wonLetters.includes(q.l)) return false;
      if (category && category !== 'all' && q.cat !== category) return false;
      return true;
    });

    if (pool.length === 0) {
      this.used.clear();
      pool = QUESTION_BANK.filter(q => {
        if (wonLetters.includes(q.l)) return false;
        if (category && category !== 'all' && q.cat !== category) return false;
        return true;
      });
    }

    if (pool.length === 0) return null;

    const preferred = pool.filter(q => !this.recentLetters.includes(q.l));
    const finalPool = preferred.length > 0 ? preferred : pool;
    const idx = Math.floor(Math.random() * finalPool.length);
    const item = finalPool[idx];

    this.used.add(item.q);
    this.recentLetters.push(item.l);
    if (this.recentLetters.length > 7) this.recentLetters.shift();

    return { question: item.q, answer: item.a, firstLetter: item.l, category: item.cat };
  }
}
